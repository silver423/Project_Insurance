public class MarketResearchingInfo {

	private String competition;
	private String customer;
	private String marketProspect;
	private String regulation;
	private String risk;
	private String trends;
	private String warrantData;

	public MarketResearchingInfo(){

	}

	public void finalize() throws Throwable {

	}

	public String getCompetition() {
		return competition;
	}

	public void setCompetition(String competition) {
		this.competition = competition;
	}

	public String getCustomer() {
		return customer;
	}

	public void setCustomer(String customer) {
		this.customer = customer;
	}

	public String getMarketProspect() {
		return marketProspect;
	}

	public void setMarketProspect(String marketProspect) {
		this.marketProspect = marketProspect;
	}

	public String getRegulation() {
		return regulation;
	}

	public void setRegulation(String regulation) {
		this.regulation = regulation;
	}

	public String getRisk() {
		return risk;
	}

	public void setRisk(String risk) {
		this.risk = risk;
	}

	public String getTrends() {
		return trends;
	}

	public void setTrends(String trends) {
		this.trends = trends;
	}

	public String getWarrantData() {
		return warrantData;
	}

	public void setWarrantData(String warrantData) {
		this.warrantData = warrantData;
	}

}